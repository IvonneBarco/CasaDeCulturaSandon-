						<p class="post-contenido text-justify font-csc">
							Sandoná es una población que se encuentra al suroccidente de Colombia en el departamento de Nariño.
							<br><br>
							<b>Nombre del municipio:</b> SANDONÁ <br>
							<b>NIT:</b> 800.099.138-5 <br>
							<b>Código Dane:</b> 52683 <br>
							<b>Gentilicio:</b> SANDONEÑOS <br><br>
							Otros nombres que ha recibido el municipio: <br><i><b>Los Llanos de Sandoná</b></i> <br>

							<br>
							<b>Fecha de fundación:</b> 24 de noviembre de 1866
							<br><br>
							<b><i>Sandoná Pueblo de origen Quillacinga.</i></b>

							<br><br>

							En el sector central Interandino hacia Yacuanquer y por la margen derecha del río Guaitara según Cieza de León,
							se extendían por las pendientes los territorios ocupados por la antigua nación Quillacinga, que eran diferentes
							a la nación de los Quillacingas de la montaña. <br><br>

							A lo largo de estas depresiones y pendientes se asentaron los pueblos de Consacá, Sandoná, El Ingenio, La Florida,
							hasta el Valle de Atríz donde los quillacingas de la montaña. <br><br>

							Estos pueblos que inicialmente no son referenciados por Cieza, aparecen luego en las relaciones geográficas de indias,
							cuando se dice que Sandoná ya existía mucho antes de la Colonia y Encarnación Moreno confirma que Sandoná era uno de
							los veinte pueblos que pertenecían a la nación Quillacinga; Bajo el dominio español Sandoná perteneció a la Doctrina
							de los Ingenios (llamada en ese entonces <i>“Doctrina del Señor Santo Domingo”</i>), al cuidados de los Dominios y la encomienda
							de Don Álvaro Arguello, con El Cacique Don Alonso y sus cuarenta y un indios tributarios durante el periodo que antecedió
							a 1582, según fecha de un informe presentado por el Corregidor Don Francisco Centelles. <br><br>

							Para esta época los habitantes de Sandoná sumaban más de <i>250</i> personas. <br><br>

							En el Archivo Histórico de Pasto <i>(Fondo Cabildo 1866- 1867, caja 48, legajo 3, fojas 2r. y 63r.)</i> se encontró un documento
							sin firma, fechado en Pasto 10 de diciembre de 1867, referente a un proyecto de ordenanza de la municipalidad de Pasto, el cual
							en su parte pertinente dice: <i>“<b>Art. 3.</b> – Erijese en Distrito la nueva población de Sandoná con el nombre de “Distrito de Mosquera”</i>
							que tendrá como capital La Villa de Sandoná; con los limites siguientes: con el Distrito de la Florida en Rió del Chaca guaico desde su
							origen hasta la confluencia con el Guaitara y con el Distrito de Consacá de la quebrada Onda desde su origen hasta el mismo Rió Guaitara. <br><br>
							
							En el mismo legajo, caja y fondo del AH. De Pasto, en foja 5r.
							Se informa que por ordenanza 23 del 21 de abril de 1865 ya se había trasladado la capital del Distrito de Consacá al caserío de los
							Llanos de Sandoná como tal, sino de Consacá. <br><br>

							Es probable que para remediar esto, es que se presentó a la municipalidad de Pasto, el proyecto de ordenanza antes citado,
							para dejar a Consacá con su propia cabecera homónima. <br><br>

							Varios autores (Mejia y Mejia, Alfredo Galiano, José Rafael Zarama, Víctor Palacios, etc.) han hablado de la ordenanza 33
							del 12 de octubre de 1868 como la que erigió a Sandoná en Distrito de Mosquera y con capital Sandoná Este dato, lo tomaron
							de un artículo publicado por José E Gómez en <i>“el Boletín Diocesano”</i> de Pasto <i><b>(No. 49)</b></i>, y en su parte pertinente
							reproducido por <b>Rafael Zarama</b> <i>(Zarama, J.R “Geografía del Departamento de Nariño.......” Imp. Del Dpto., Pasto1927, p.64)</i>
							dice <i>“en ese año de 1868 se estableció también la parroquia eclesiástica de los Llanos de Sandoná o Los Llanos”.</i> <br><br>

							El Distrito de Mosquera según estos datos tuvo los siguientes límites: hacia el <b>Norte</b>, La Florida y El Tambo, río Cachi por medio. 
							<b>Sur</b>, Consacá, quebrada Onda, faldas del Galeras por medio. <b>Oriente</b>, con el Distrito de Pasto, por las cimas del Galeras por medio. 
							<b>Occidente</b>, con Linares y Ancuya, río Guaitara por medio. <br><br>

							La época disposición de los vocales de la municipalidad de Pasto para que Sandoná se convirtiera en Distrito independiente
							de Consacá (tal vez por motivos políticos o electorales), se evidencia cuando en el <i><b>Art. 9</b> de la ordenanza No 18 del 23 de
							febrero de 1874</i> sobre división territorial del municipio de Pasto, se refunde a Sandoná en Consacá, en los siguientes términos
							<i>“Consacá su capital la Villa de los Llanos.</i> <br><br>

							Por lo tanto no se reconoce cuando fue restablecido como Distrito de Los Llanos de Sandoná, pero Gómez dice que por ordenanza
							6 de 1898 de la Asamblea Departamental del Cauca fue segregado el corregimiento de El Ingenio que pertenecía al Distrito de
							La Florida y anexado al municipio o Distrito municipal de Sandoná, incluyendo en dicho corregimiento a las parcialidades de
							Santa Rosa de Anganoy, y Santa Bárbara de Cunchuy, antiguo Matacunchuy, quedando como limite de los Distritos de Chaca guaico.
							Gerardo Maya investigo que en 1876 llamabas distrito del Rosario (Maya Gerardo, La realidad histórica y social de Sandoná, Edith GRAFICOLOR,
							Pasto. 1988, p.45) y que en el año de 1878 recobró su antiguo nombre de Sandoná, aunque la gente solía llamarlo los Llanos de Sandoná
							(Pero no presenta evidencias. En definitiva, según estos últimos datos, Sandoná con anterioridad a 1898 y con posterioridad a 1874,
							debió ser erigido definitivamente como distrito del municipio de Pasto. <br>
							 

