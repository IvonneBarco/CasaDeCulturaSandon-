							<i><b>DECRETO N0 318 del 7 de julio de 1994.</b></i><br><br>

						<p class="post-contenido text-justify font-csc">
							Las características del escudo son las siguientes: <br>
							Forma rectangular,
							siendo sus vértices superiores cortados en semicírculo hacia el interior
							del símbolo y su base curva con terminación en punta hacia abajo en el centro
							del estandarte; su interior lo conforma una punta de lanza dentada, hacia arriba,
							ubicándose su ángulo principal en el centro de la línea superior y los ángulos
							laterales al iniciar las curvaturas de la base, esta franja será de color blanco
							y tendrá en su centro arpegios musicales; la parte inferior será de color rojo y
							tendrá en su centro un sol naciente, con una cruz de color morado y un libro abierto
							con las palabras <i>Dios y Amor</i> en las páginas abiertas; las franjas laterales serán de
							color verde y de cada una surgirán manos que empuñaran en el lado izquierdo una rama
							de café y en el derecho una de caña; sobre lo antes descrito se ubicará una estrella
							de cinco puntas, cuyos extremos serán blancos y dorados y en la parte inferior de la
							base una banda flotante de tres dobleces, de color amarillo en su exterior, y rojo
							en su interior, con las palabras <i>Dios – Patria – Trabajo</i> en Letra de Estilo.

							<br><br>

							<b>SIMBOLOGÍA</b><br><br>

							La simbología del escudo es la siguiente: <br>

							<ul>
								<li class="post-contenido text-justify font-csc">
									<b>Estrella dorada y blanca de cinco puntas:</b>
									Representan los principios de UNIDAD, JUSTICIA, DEMOCRACIA, LIBERTAD Y PROGRESO.<br><br>
								</li>

								<li class="post-contenido text-justify font-csc"><b>
									Franjas verdes y su contenido:</b>
									Aluden la esperanza, la agricultura, la laboriosidad de las gentes sandoneñas y los cultivos de mayor importancia.<br><br>
								</li>

								<li class="post-contenido text-justify font-csc">
									<b>Franja blanca y su contenido:</b>
									Hacen referencia a la Paz y la tradición cultural y artística de la región.<br><br>
								</li>

								<li class="post-contenido text-justify font-csc">
									<b>Franja roja y su interior:</b>
